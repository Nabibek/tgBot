// –ü–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫–∞—è –æ—Ç–ø—Ä–∞–≤–∫–∞ —Ü–∏—Ç–∞—Ç –ø–æ–¥–ø–∏—Å—á–∏–∫–∞–º
func sendQuotesPeriodically(bot *tgbotapi.BotAPI) {
	// –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ü–∏—Ç–∞—Ç—ã –≤ 9:00 –∏ 18:00 (–≤ –≤–∞—à–µ–º –ª–æ–∫–∞–ª—å–Ω–æ–º –≤—Ä–µ–º–µ–Ω–∏)
	ticker := time.NewTicker(1 * time.Hour)
	defer ticker.Stop()

	for range ticker.C {
		now := time.Now()
		hour := now.Hour()

		// –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ü–∏—Ç–∞—Ç—ã –≤ 9:00 –∏ 18:00
		if hour == 9 || hour == 18 {
			if now.Minute() < 5 { // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ–±—ã –æ—Ç–ø—Ä–∞–≤–∏—Ç—å –æ–¥–∏–Ω —Ä–∞–∑ –≤ —á–∞—Å
				mutex.RLock()
				subscriberCount := len(subscribers)
				logAndPrint("üì® –†–ê–°–°–´–õ–ö–ê –¶–ò–¢–ê–¢ –í %d:00 | –ü–æ–¥–ø–∏—Å—á–∏–∫–æ–≤: %d", hour, subscriberCount)

				for chatID, subscriber := range subscribers {
					quote := quotes[rand.Intn(len(quotes))]
					text := "‚ú® *–í–∞—à–∞ –µ–∂–µ–¥–Ω–µ–≤–Ω–∞—è –º–æ—Ç–∏–≤–∞—Ü–∏—è:*\n\n" +
						quote + "\n\n" +
						"_–ò–º–µ–π—Ç–µ –≤–µ—Ä—É –≤ —Å–µ–±—è! üí™_"

					msg := tgbotapi.NewMessage(chatID, text)
					msg.ParseMode = "Markdown"

					if _, err := bot.Send(msg); err != nil {
						logAndPrint("‚ùå –û–®–ò–ë–ö–ê –û–¢–ü–†–ê–í–ö–ò: %s (%s) - ID: %d | %v",
							subscriber.FirstName,
							subscriber.FirstName,
							chatID,
							err)
					}
				}
				mutex.RUnlock()

				logAndPrint("‚úÖ –†–ê–°–°–´–õ–ö–ê –ó–ê–í–ï–†–®–ï–ù–ê: –û—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ %d —Ü–∏—Ç–∞—Ç –≤ %d:00", subscriberCount, hour)
			}
		}
	}
}
