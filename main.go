// –û—Ç–ø–∏—Å–∫–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
func handleUnsubscribe(bot *tgbotapi.BotAPI, message *tgbotapi.Message) {
	mutex.Lock()
	defer mutex.Unlock()

	if _, exists := subscribers[message.Chat.ID]; !exists {
		logAndPrint("‚ö†Ô∏è  –ü–û–ü–´–¢–ö–ê –û–¢–ü–ò–°–ö–ò –ù–ï –ü–û–î–ü–ò–°–ê–ù–ù–û–ì–û: %s (%s) - ID: %d",
			message.From.FirstName,
			message.From.UserName,
			message.From.ID)
		msg := tgbotapi.NewMessage(message.Chat.ID, "‚ùå –í—ã –Ω–µ –ø–æ–¥–ø–∏—Å–∞–Ω—ã –Ω–∞ —Ü–∏—Ç–∞—Ç—ã.")
		bot.Send(msg)
		return
	}

	delete(subscribers, message.Chat.ID)

	logAndPrint("‚ùå –û–¢–ü–ò–°–ö–ê: %s (%s) - ID: %d | –û—Å—Ç–∞–ª–æ—Å—å –ø–æ–¥–ø–∏—Å—á–∏–∫–æ–≤: %d",
		message.From.FirstName,
		message.From.UserName,
		message.From.ID,
		len(subscribers))

	msg := tgbotapi.NewMessage(message.Chat.ID, "‚ùå –í—ã –æ—Ç–ø–∏—Å–∞–ª–∏—Å—å –æ—Ç —Ü–∏—Ç–∞—Ç.")
	bot.Send(msg)
}

